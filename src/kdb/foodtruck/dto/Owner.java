package kdb.foodtruck.dto;

public class Owner {
	private int		ownerNum; 
	private	String	ownerId;
	private	String 	ownerPwd;
	private	String 	ownerReg;
	private	String	ownerName;
	private	String 	ownerPnum;
	private	String	ownerRpic;
	private	String	ownerEmail;
	private	String	ownerAddr;
	
	public int getOwnerNum() {
		return ownerNum;
	}
	public void setOwnerNum(int ownerNum) {
		this.ownerNum = ownerNum;
	}
	public String getOwnerId() {
		return ownerId;
	}
	public void setOwnerId(String ownerId) {
		this.ownerId = ownerId;
	}
	public String getOwnerPwd() {
		return ownerPwd;
	}
	public void setOwnerPwd(String ownerPwd) {
		this.ownerPwd = ownerPwd;
	}
	public String getOwnerReg() {
		return ownerReg;
	}
	public void setOwnerReg(String ownerReg) {
		this.ownerReg = ownerReg;
	}
	public String getOwnerName() {
		return ownerName;
	}
	public void setOwnerName(String ownerName) {
		this.ownerName = ownerName;
	}
	public String getOwnerPnum() {
		return ownerPnum;
	}
	public void setOwnerPnum(String ownerPnum) {
		this.ownerPnum = ownerPnum;
	}
	public String getOwnerRpic() {
		return ownerRpic;
	}
	public void setOwnerRpic(String ownerRpic) {
		this.ownerRpic = ownerRpic;
	}
	public String getOwnerEmail() {
		return ownerEmail;
	}
	public void setOwnerEmail(String ownerEmail) {
		this.ownerEmail = ownerEmail;
	}
	public String getOwnerAddr() {
		return ownerAddr;
	}
	public void setOwnerAddr(String ownerAddr) {
		this.ownerAddr = ownerAddr;
	}
	
	
}
